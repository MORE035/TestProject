package basicjava;

public class Print_N_Numbers {

	public static void main(String args[]) {
		
		int i=1;
		while(i>=0) {
			System.out.println(i);
			i=i+2;
			if(i==11) {
				break;
				
			}
		}
		System.out.println(" ");

		
		for(int j=1;j<=10;j=j+2) {
			
			System.out.println(j);
			
		}
	
		
		
		
	}

}
